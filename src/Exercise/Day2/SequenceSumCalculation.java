package Exercise.Day2;

public class SequenceSumCalculation {
    public static void main(String[] args) {
        int sum;
        int n = 10;
        sum = 3 * n * n - 12 * n;
        System.out.println("Value of 10 - th sum = " + sum);
        n = 15;
        sum = 3 * n * n - 12 * n;
        System.out.println("Value of 15 - th sum = " + sum);
    }
}
